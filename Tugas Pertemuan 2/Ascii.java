public class Ascii {
    public static void main(String[] args) {

        int asciiX = (int) 'X';
        int asciiY = (int) 'y';

        int sum = asciiX + asciiY;

        System.out.println("Nilai ASCII dari 'X' : " + asciiX);
        System.out.println("Nilai ASCII dari 'y' : " + asciiY);
        System.out.println("Jumlah nilai ASCII dari 'X' dan 'y' : " + sum);
    }
}